#include <vectrex.h>
#include "print.h"

// ---------------------------------------------------------------------------
// print long hex value at absolute coordinates (y, x)

void print_hex(int y, int x, unsigned int z)
{
	volatile char message[3];
	message[2] = '\x80';
	if ((message[1] = (z & 0b00001111) + '0') > '9')
	{
		message[1] += 7;
	}
	z >>= 4;
	if ((message[0] = z + '0') > '9')
	{
		message[0] += 7;
	}
	reset_print_position();
	Print_Str_d(y, x, (void*) &message[0]);
}

