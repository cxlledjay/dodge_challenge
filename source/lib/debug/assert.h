// ***************************************************************************
// assert - run-time assertion checking
// ***************************************************************************

#pragma once
#include "debug.h"

// ---------------------------------------------------------------------------
// assert(expr);
// ---------------------------------------------------------------------------

#ifndef NDEBUG
#define assert(expr) \
	if (!(expr)) \
		_f_debug("ASSERT FAILED!\x80", DEBUG_TYPE_UNSIGNED_INT, 0L, __FILE__, sizeof(__FILE__), (unsigned long int) __LINE__);
#else
#define assert(expr)
#endif

